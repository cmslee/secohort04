
// --- Primitive type values --- //

// Data type String
var msg = "Hello World!";
console.log(msg);

// Data type String
var firstLetter = 'a';
console.log(firstLetter);

// Data type Number
var age = 18;
console.log(age);

// Data type Boolean
var isAdult = true;
var isChild = false;
console.log(isAdult);
console.log(isChild);

// Data type Null Value (typeof is object until number value is assigned)
var accountNumber = null;
// to re-assign the value of the variable
accountNumber = 123456;
console.log(accountNumber)

// Data type Undefined Value
var userAddress;
console.log(userAddress)


// --- Reference type values --- //

//Arrays (typeof is object)
var namesArray = ["Colton", "Anthony", "Ranciel", "Faye"];
var agesArray = [23, 12, 8, 11];
console.log(namesArray)


//Objects (typeof is object)
var userDetails = {
    userName: "John",
    userLastname: "Wright",
    userAge: 22
};
console.log(userDetails)


